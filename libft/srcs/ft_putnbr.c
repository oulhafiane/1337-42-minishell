#include "libft.h"

void	ft_putnbr(int n)
{
	long nb;

	nb = (long)n;
	if (n < 0)
	{
		ft_putchar('-');
		nb = nb * -1;
	}
	if (nb / 10 == 0)
		ft_putchar(nb + '0');
	else
	{
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + '0');
	}
}
